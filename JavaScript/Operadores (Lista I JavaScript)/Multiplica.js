/*2) Faça um programa no qual o usuário digite dois números e mostre na
tela a multiplicação desses números.*/

let NUm, NDois
let MultiplicaNumeros

//Entrada de Dados
NUm = Number(prompt('Digite o primeiro número: '))
NDois = Number(prompt('Digite o segundo número:'))

//Cálculos
MultiplicaNumeros = NUm * NDois

//Saída de Dados
alert ('Total: ' + MultiplicaNumeros)